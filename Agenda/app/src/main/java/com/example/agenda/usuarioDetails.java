package com.example.agenda;

public class usuarioDetails {
    String name;
    String senha;

    public usuarioDetails() {
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getName() {
        return name;
    }

    public String getSenha() {
        return senha;
    }
}
